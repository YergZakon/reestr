"use client";
/* Встроенная страница «Помощь» — пошаговые инструкции для сотрудников госорганов
   (роли «Модератор» и «Аналитик»). Контент выверен по реальному UI реестра. */
import { useState } from "react";

type Aud = "all" | "expert" | "moderator" | "admin";
interface Section { id: string; title: string; aud: Aud; }

const SECTIONS: Section[] = [
  { id: "start", title: "Начало работы", aud: "all" },
  { id: "roles", title: "Роли и права", aud: "all" },
  { id: "review", title: "Ревью требований", aud: "expert" },
  { id: "catalog", title: "Каталог и поиск", aud: "all" },
  { id: "organs", title: "Органы и НПА", aud: "all" },
  { id: "analysts", title: "Управление аналитиками", aud: "moderator" },
  { id: "submit", title: "Подача НПА", aud: "moderator" },
  { id: "assign", title: "Назначение комитетов", aud: "moderator" },
  { id: "notifications", title: "Уведомления", aud: "all" },
  { id: "glossary", title: "Термины и статусы", aud: "all" },
];

const AUD_LABEL: Record<Aud, string> = { all: "Все", expert: "Аналитик", moderator: "Модератор", admin: "МНЭ" };

/* — вспомогательные примитивы верстки — */
function UI({ children }: { children: React.ReactNode }) { return <span className="reg-help-ui">{children}</span>; }
function Note({ children, warn }: { children: React.ReactNode; warn?: boolean }) {
  return <div className={"reg-help-note" + (warn ? " warn" : "")}>{children}</div>;
}
function Steps({ children }: { children: React.ReactNode }) { return <ol className="reg-help-steps">{children}</ol>; }
function AudTag({ aud }: { aud: Aud }) {
  if (aud === "all") return null;
  return <span className={"reg-help-aud reg-help-aud-" + aud}>{AUD_LABEL[aud]}</span>;
}

export default function HelpMode({ role }: { role?: string }) {
  const [sec, setSec] = useState("start");
  const roleName = role === "admin" ? "МНЭ (Администратор)" : role === "moderator" ? "Модератор" : "Аналитик";

  return (
    <div className="reg-shell">
      <aside className="reg-sidebar reg-help-nav">
        <div className="reg-help-nav-head">
          <div className="reg-help-nav-title">Содержание</div>
          <div className="reg-help-nav-role">Вы вошли как: <b>{roleName}</b></div>
        </div>
        {SECTIONS.map((s) => (
          <button key={s.id} className={"reg-help-nav-item" + (sec === s.id ? " on" : "")} onClick={() => setSec(s.id)}>
            <span className="reg-help-nav-item-t">{s.title}</span>
            <AudTag aud={s.aud} />
          </button>
        ))}
      </aside>

      <main className="reg-content reg-help">
        {sec === "start" && <Start />}
        {sec === "roles" && <Roles />}
        {sec === "review" && <Review />}
        {sec === "catalog" && <Catalog />}
        {sec === "organs" && <Organs />}
        {sec === "analysts" && <Analysts />}
        {sec === "submit" && <Submit />}
        {sec === "assign" && <Assign />}
        {sec === "notifications" && <Notifications />}
        {sec === "glossary" && <Glossary />}
      </main>
    </div>
  );
}

