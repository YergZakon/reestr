def build_mapping() -> dict[str, str]:
    """Возвращает словарь {prefix: sphere_code}."""
    mapping = {}
    for s in LAND:
        mapping[s.strip()] = "land"
    for s in TRANSPORT:
        mapping[s.strip()] = "transport"
    for s in ECOLOGY:
        mapping[s.strip()] = "ecology"
    return mapping


def classify(service_name: str, mapping: dict[str, str]) -> tuple[str, str]:
    """Возвращает (sphere_code, mapped_by). Использует startswith."""
    name = service_name.strip()
    for prefix, sphere in mapping.items():
        if name.startswith(prefix):
            return sphere, "manual"
    return "other", "manual"


def main():
    db_url = os.environ.get("DATABASE_URL")
    if not db_url:
        sys.exit("ERROR: DATABASE_URL not set.")

    mapping = build_mapping()

    with psycopg2.connect(db_url) as conn:
        with conn.cursor() as cur:
            cur.execute("SELECT service_name FROM service_to_sphere_map ORDER BY service_name")
            services = [r[0] for r in cur.fetchall()]
            print(f"[map] Услуг в service_to_sphere_map: {len(services)}")

            updates = []
            counts = {"land": 0, "transport": 0, "ecology": 0, "other": 0}
            for svc in services:
                sphere, mapped_by = classify(svc, mapping)
                counts[sphere] += 1
                updates.append((sphere, mapped_by, svc))

            cur.executemany(
                """
                UPDATE service_to_sphere_map
                   SET sphere_code = %s,
                       mapped_by   = %s,
                       mapped_at   = NOW()
                 WHERE service_name = %s
                """,
                updates,
            )

            # Заполняем sphere_code в source_fragments для слоя 'services'
            cur.execute("""
                UPDATE source_fragments sf
                   SET sphere_code = m.sphere_code
                  FROM service_to_sphere_map m
                 WHERE sf.source_layer = 'services'
                   AND sf.service_name = m.service_name
                   AND m.sphere_code IS NOT NULL
            """)
            updated_fragments = cur.rowcount
            conn.commit()

            print()
            print("Маппинг услуг к сферам:")
            for sphere, count in counts.items():
                print(f"  {sphere:<10} {count:>4}")
            print(f"\n[map] Обновлено source_fragments (слой services): {updated_fragments}")

            # Срез фрагментов услуг по сферам
            cur.execute("""
                SELECT sphere_code, COUNT(*) AS cnt
                  FROM source_fragments
                 WHERE source_layer = 'services'
                 GROUP BY sphere_code
                 ORDER BY cnt DESC
            """)
            print("\nФрагментов слоя 'services' по сферам:")
            for sphere_code, cnt in cur.fetchall():
                label = sphere_code if sphere_code else "(не отнесено)"
                print(f"  {label:<15} {cnt:>5}")


if __name__ == "__main__":
    main()
