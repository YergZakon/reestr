def main() -> None:
    db_url = os.environ.get("DATABASE_URL")
    if not db_url:
        sys.exit("ERROR: DATABASE_URL not set")

    with psycopg2.connect(db_url) as conn:
        with conn.cursor() as cur:
            cur.execute("SELECT id FROM scenarios WHERE code = 'agro_land_use'")
            row = cur.fetchone()
            if not row:
                sys.exit("ERROR: scenario 'agro_land_use' not found")
            scenario_id = row[0]

            # Очистка
            cur.execute("DELETE FROM scenario_cards WHERE scenario_id = %s", (scenario_id,))

            inserted = 0
            not_found = []
            for fragment_id, ordering, is_required, notes in ROUTE:
                cur.execute(
                    """
                    INSERT INTO scenario_cards (scenario_id, card_id, is_required, ordering, notes)
                    SELECT %s, sf.canonical_card_id, %s, %s, %s
                      FROM source_fragments sf
                     WHERE sf.id = %s AND sf.canonical_card_id IS NOT NULL
                    """,
                    (scenario_id, is_required, ordering, notes, fragment_id),
                )
                if cur.rowcount == 0:
                    not_found.append(fragment_id)
                else:
                    inserted += 1

            cur.execute("UPDATE scenarios SET is_published = TRUE WHERE id = %s", (scenario_id,))
            conn.commit()

            print(f"[agro] Вставлено связей: {inserted}")
            if not_found:
                print(f"[agro] Не найдены fragment_ids: {not_found}")

            cur.execute(
                """
                SELECT sc.ordering, sc.is_required, c.card_code, c.short_title
                  FROM scenario_cards sc
                  JOIN requirement_cards c ON c.id = sc.card_id
                 WHERE sc.scenario_id = %s
                 ORDER BY sc.ordering
                """,
                (scenario_id,),
            )
            print()
            print('Маршрут «Сельхозпроизводитель / КФХ»:')
            print('-' * 100)
            for row in cur.fetchall():
                order, req, code, title = row
                flag = '★' if req else 'опц'
                print(f'  [{order:>3}] {flag:<4} {code}  {(title or "")[:75]}')


if __name__ == "__main__":
    main()
