-- 019_sphere_oked_map.sql — карта СФЕРА → ОКЭД-охват для точной отраслевой выдачи
-- Применяется: scripts/registry/map_sphere_oked.py --apply
--
-- Проблема: привязка отраслевой выдачи по букве-секции (A–S) смешивала под-отрасли
-- (детсад и ВУЗ оба в P → детсад видел требования к вузам). sphere_code под-отрасли
-- различает (mnvo=высшее ≠ education_general=дошкольное/среднее), но выдача его не
-- использовала.
--
-- Решение: у каждой сферы — охват по ОКЭД (разделы/группы) либо признак сквозной.
--   is_horizontal=true  → применима ко всем видам деятельности (труд, налоги, пожарка…)
--   oked_prefixes[]     → для отраслевой: ОКЭД-префиксы охвата (mnvo→{854,855,72},
--                         education_general→{851,852,853}, mz_zdrav→{86}, transport→{49..53}).
-- Бизнес-выдача (api/business/requirements, conclusion): отраслевые требования =
-- требования сфер, чей oked_prefix является префиксом выбранного ОКЭД.
-- Детсад 8510 → '851%' → education_general (НЕ mnvo). Если выбрана секция-плитка
-- (без ОКЭД) — fallback на букву-секцию.

ALTER TABLE spheres ADD COLUMN IF NOT EXISTS oked_prefixes TEXT[];
ALTER TABLE spheres ADD COLUMN IF NOT EXISTS is_horizontal BOOLEAN;

-- Значения проставляются скриптом map_sphere_oked.py (DeepSeek + ручная коррекция OVERRIDES):
-- горизонтальные: труд, налоги, бухучёт, пожарка, регистрация, госуслуги, статистика,
--   антикоррупция, конкуренция, таможня, экология, языки, оборона, прочие, трудоустройство,
--   общественный порядок, бюджет/госзакупки;
-- отраслевые: образование (851-853 / 854-855), здравоохранение (86), транспорт (49-53),
--   торговля (45-47), обрабатка (10-33), строительство (41-43), недра/нефть (05-09…),
--   общепит (561-563), финрынок (64-66), СМИ (58-63), сельское/водные/лесные и др.
